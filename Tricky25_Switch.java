package com.tricky;

public class Tricky25_Switch {

	public static void main(String[] args) {

		short num = 30;
		switch(num){
		case 10: System.out.println("10");
		case 20: System.out.println("20");
		case 30: System.out.println("30");
		case 40: System.out.println("40");
		}
	}

}
